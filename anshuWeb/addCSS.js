function addCSS1() {

  
  let starButton = document.querySelector('.starss1');
  let checkbox1 = document.querySelector('.checkbox1'); // Assuming the checkbox has an id 'checkbox'

  if (checkbox1.checked) {
    starButton.classList.remove('hover1');
      starButton.classList.add('hover');
       

    }
  
   else {
    starButton.classList.remove('hover');
    starButton.classList.add('hover1');}
  
}



